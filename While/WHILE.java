public class WHILE{
	public static void main(String args[]){
		
		int i = 1;           //Declaramos la variable i en 1
		
		while(i < 10){                   //Mientras i sea menor que 10
			System.out.print(i + ", ");  //Escribe el valor
			i+=2;                         //Y escribelo de 2 en 2
			
		} 
		
	}
	
	
}

